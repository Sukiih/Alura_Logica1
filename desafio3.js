// Crea un contador que comience en 1 y vaya hasta 10 usando un bucle 'while'. 
// Muestra cada número.
let i = 1
while (i<=10){
    console.log(i);
    i++;
}

// Crea un contador que comience en 10 y vaya hasta 0 usando un bucle 'while'. 
// Muestra cada número.
let count = 10;
while (count >= 0) {
    console.log(count);
    count--;
}

